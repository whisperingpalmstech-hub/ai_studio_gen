const langs = ['es', 'fr', 'de', 'zh', 'ja', 'ar', 'ru', 'pt', 'it', 'ko'];

async function translateText(text, targetLang) {
    if (!text || typeof text !== 'string') return text;
    try {
        const url = `https://translate.googleapis.com/translate_a/single?client=gtx&sl=en&tl=${targetLang}&dt=t&q=${encodeURIComponent(text)}`;
        const response = await fetch(url);
        const data = await response.json();
        let translatedText = '';
        if (data && data[0]) {
            data[0].forEach(segment => {
                if (segment[0]) translatedText += segment[0];
            });
        }
        return translatedText || text;
    } catch (e) {
        console.log("Error translating:", text, targetLang);
        return text;
    }
}

async function main() {
    let output = '';
    for (const lang of langs) {
        console.log(`Translating ${lang}...`);
        output += `    ${lang}: {\n`;
        const keys = Object.keys(en);
        for (let i = 0; i < keys.length; i++) {
            const key = keys[i];
            const val = en[key];
            const t = await translateText(val, lang);
            output += `        ${key}: ${JSON.stringify(t)}`;
            if (i < keys.length - 1) output += ',\n';
            else output += '\n';
        }
        output += `    },\n`;
    }
    fs.writeFileSync('translated.txt', output);
    console.log("Done");
}
main();
